    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String dt1, dt2;
        if(jDateChooser1.getDate() != null){
            dt1 = validacao.mascaraData(Validacao.data_americano, jDateChooser1.getDate());
        }else{
            dt1 = "";
        }
        if(jDateChooser2.getDate() != null){
            dt2 = validacao.mascaraData(Validacao.data_americano, jDateChooser2.getDate());
        }else{
            dt2 = "";
        }
        ambientes = arn.procurarAmbiente(jTextField3.getText(), dt1, dt2, funcionario.getEmpresa().getCodempresa());
        if (ambientes != null && !ambientes.isEmpty()) {
            listarAmbiente();
        } else {
            JOptionPane.showMessageDialog(null, "Nada encontrado!!!", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void batualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_batualizarActionPerformed
        if (ambiente != null && ambiente.getCodambiente() > 0) {
            setaObjeto();
            String msg = arn.atualizar(ambiente);
            if (msg != null && !msg.isEmpty()) {
                JOptionPane.showMessageDialog(null, msg, "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Ambiente atualizado com sucesso", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione antes um ambiente", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_batualizarActionPerformed

    private void bexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bexcluirActionPerformed
        Thread tacao = new Thread(() -> {
            if (ambiente != null && ambiente.getCodambiente() != 0) {
                int response = JOptionPane.showConfirmDialog(null, "Voc√™ quer excluir o ambiente?", "Confirma√ß√£o de excluir:",
                        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (response == JOptionPane.YES_OPTION) {
                    String msg = arn.excluir(ambiente);
                    if (msg != null && !msg.isEmpty()) {
                        JOptionPane.showMessageDialog(null, msg, "Erro", JOptionPane.ERROR_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(null, "Ambiente cadastrado com sucesso", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Selecione antes um ambiente", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
        tacao.start();
    }//GEN-LAST:event_bexcluirActionPerformed

