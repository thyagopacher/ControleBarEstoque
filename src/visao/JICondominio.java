    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Thread tacao = new Thread(() -> {
            if(empresa != null && empresa.getCodempresa() > 0){
                String msg = eRN.atualizar(empresa);
                if(msg != null && !msg.isEmpty()){
                    JOptionPane.showMessageDialog(null, msg, "Condominio", JOptionPane.ERROR_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Condominio atualizado com sucesso!!!", "Condominio", JOptionPane.INFORMATION_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Selecione antes um condominio", "Condominio", JOptionPane.ERROR_MESSAGE);
            }
        });
        tacao.start();
    }//GEN-LAST:event_jButton6ActionPerformed

