package vo;

/**
 *
 * @author ThyagoHenrique
 */
public class Pagina {
    private int codpagina;
    private String nome;
    private String link;
    private String titulo;
    private int codmodulo;
    private int codpai;
    private String dtcadastro;
    private String comcodigo;

    /**
     * @return the codpagina
     */
    public int getCodpagina() {
        return codpagina;
    }

    /**
     * @param codpagina the codpagina to set
     */
    public void setCodpagina(int codpagina) {
        this.codpagina = codpagina;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the link
     */
    public String getLink() {
        return link;
    }

    /**
     * @param link the link to set
     */
    public void setLink(String link) {
        this.link = link;
    }

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the codmodulo
     */
    public int getCodmodulo() {
        return codmodulo;
    }

    /**
     * @param codmodulo the codmodulo to set
     */
    public void setCodmodulo(int codmodulo) {
        this.codmodulo = codmodulo;
    }

    /**
     * @return the codpai
     */
    public int getCodpai() {
        return codpai;
    }

    /**
     * @param codpai the codpai to set
     */
    public void setCodpai(int codpai) {
        this.codpai = codpai;
    }

    /**
     * @return the dtcadastro
     */
    public String getDtcadastro() {
        return dtcadastro;
    }

    /**
     * @param dtcadastro the dtcadastro to set
     */
    public void setDtcadastro(String dtcadastro) {
        this.dtcadastro = dtcadastro;
    }

    /**
     * @return the comcodigo
     */
    public String getComcodigo() {
        return comcodigo;
    }

    /**
     * @param comcodigo the comcodigo to set
     */
    public void setComcodigo(String comcodigo) {
        this.comcodigo = comcodigo;
    }
}
